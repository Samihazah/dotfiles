#!/bin/sh

# This is a list of aliases to be sourced with bash/zsh.
# Good for sanity.
# Place in $SHELL directory.

# Common user commands:
alias ls='ls --color=auto --group-directories-first'
alias la='ls -A --color=auto --group-directories-first'
alias ..='cd ..'
alias mv='mv -i'
alias rm='rm -i'
